package org.four.bean;

public class bodyBean {
    public bodyBean() {}

    private double h;
    private double w;
    private String mess;
    private String woman;
    private String man;

    public double getH() {
        return h;
    }
    public void setH(double h) {
        this.h = h;
    }

    public double getW() {
        return w;
    }
    public void setW(double w) {
        this.w = w;
    }
    
    public String getMess() {
        return mess;
    }
    public void setMess(String mess) {
        this.mess = mess;
    }

    public String getWoman() {
        return woman;
    }
    public void setWoman(String woman) {
        this.woman = woman;
    }

    public String getMan() {
        return man;
    }
    public void setMan(String man) {
        this.man = man;
    }
}